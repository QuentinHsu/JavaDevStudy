package OverallOperation;
// 自增/自减
public class SelfIncreaseAndDecrease {
    public static void main(String[] args) {
        int n = 3300;
        System.out.println("n 的初始值：" + n);
        n ++;   // => n = n + 1
        System.out.println("n ++：" + n);
        n --;   // => n = n -1
        System.out.println("n --：" + n);
    }
}
